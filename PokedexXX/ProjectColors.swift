import Foundation
import UIKit

extension UIColor {
    func charamanderRed() -> UIColor {
        return UIColor(red: 253/255.0, green: 72/255.0, blue: 79/255.0, alpha: 1.0)
    }
    
    func squirtleBlue() -> UIColor {
        return UIColor(red: 60/255.0, green: 78/255.0, blue: 255/255.0, alpha: 1.0)
    }
    
    func bulbasaurGreen() -> UIColor {
        return UIColor(red: 110/255.0, green: 192/255.0, blue: 157/255.0, alpha: 1.0)
    }
    
    func pikachuYellow() -> UIColor {
        return UIColor(red: 255/255.0, green: 208/255.0, blue: 29/255.0, alpha: 1.0)
    }
    
    func onyxGrey() -> UIColor {
        return UIColor(red: 170/255.0, green: 170/255.0, blue: 170/255.0, alpha: 1.0)
    }
}